package org.example;

public class Calculator {
    public double add(int a, int b) {
        return a + b;
    }

    public double divide(double a, double b) {
        return a / b;
    }

    public double multiple(double a, double b) {
        return a * b;
    }

    public double square(double a) {
        return a * a;
    }
}
